def ReduceGapFromPageTop(wch_section='main page'):
    """
    ReduceGapFromPageTop ajusta el espacio entre la parte superior de la p√°gina y las secciones espec√≠ficas.
    Par√°metros:
    - wch_section (str): La secci√≥n para la cual se desea ajustar el espacio. Puede ser 'main page' para la p√°gina principal,
                         'sidebar' para la barra lateral o 'all' para ambas.
    """
    if wch_section == 'main page':
        # Si la secci√≥n especificada es la p√°gina principal, ajusta el relleno superior de los contenedores de bloques en la p√°gina principal.
        st.markdown("<style> div[class^='block-container'] { padding-top: 2rem; } </style>", True)
    elif wch_section == 'sidebar':
        # Si la secci√≥n especificada es la barra lateral, ajusta el relleno superior de los elementos en la barra lateral.
        st.markdown("<style> div[class^='st-emotion-cache-10oheav'] { padding-top: 0rem; } </style>", True)
    elif wch_section == 'all':
        # Si se especifica 'all', ajusta tanto la p√°gina principal como la barra lateral.
        st.markdown("<style> div[class^='block-container'] { padding-top: 2rem; } </style>", True)  # P√°gina principal
        st.markdown("<style> div[class^='st-emotion-cache-10oheav'] { padding-top: 0rem; } </style>", True)  # Barra lateral


def Leaderboard(what_to_do):
    """
    Leaderboard administra el tablero de l√≠deres del juego.

    Par√°metros:
    - what_to_do (str): La acci√≥n a realizar ('create' para crear, 'write' para escribir, 'read' para leer).

    """

    if what_to_do == 'create':
        # Si se solicita crear el tablero de l√≠deres y el nombre del jugador est√° presente, crea un nuevo archivo JSON si no existe.
        if mystate.GameDetails[3] != '' and not os.path.isfile(vpth + 'leaderboard.json'):
            tmpdict = {}
            json.dump(tmpdict, open(vpth + 'leaderboard.json', 'w'))  # Escribir archivo

    elif what_to_do == 'write':
        # Si se solicita escribir en el tablero de l√≠deres y el nombre del jugador est√° presente, registra la puntuaci√≥n en el tablero.
        if mystate.GameDetails[3] != '':
            if os.path.isfile(vpth + 'leaderboard.json'):
                leaderboard = json.load(open(vpth + 'leaderboard.json'))  # Leer archivo
                leaderboard_dict_lngth = len(leaderboard)

                # Agregar el nuevo puntaje al tablero y ordenarlo de forma descendente.
                leaderboard[str(leaderboard_dict_lngth + 1)] = {'NameCountry': mystate.GameDetails[3],
                                                                'HighestScore': mystate.myscore}
                leaderboard = dict(sorted(leaderboard.items(), key=lambda item: item[1]['HighestScore'], reverse=True))

                # Mantener solo los primeros 4 puntajes.
                if len(leaderboard) > 4:
                    for i in range(len(leaderboard) - 4):
                        leaderboard.popitem()  # Eliminar la √∫ltima entrada

                json.dump(leaderboard, open(vpth + 'leaderboard.json', 'w'))  # Escribir archivo

    elif what_to_do == 'read':
        # Si se solicita leer el tablero de l√≠deres y el nombre del jugador est√° presente, muestra los mejores puntajes.
        if mystate.GameDetails[3] != '':
            if os.path.isfile(vpth + 'leaderboard.json'):
                leaderboard = json.load(open(vpth + 'leaderboard.json'))  # Leer archivo

                # Ordenar el tablero de l√≠deres de forma descendente.
                leaderboard = dict(sorted(leaderboard.items(), key=lambda item: item[1]['HighestScore'], reverse=True))

                # Mostrar los mejores 4 puntajes en columnas separadas.
                sc0, sc1, sc2, sc3 = st.columns((3, 4, 4, 4))
                rknt = 0
                for vkey in leaderboard.keys():
                    if leaderboard[vkey]['NameCountry'] != '':
                        rknt += 1
                        if rknt == 1:
                            sc0.write('üèÜ Past Winners:')
                            sc1.write(
                                f"ü•á | {leaderboard[vkey]['NameCountry']}: :red[{leaderboard[vkey]['HighestScore']}]")
                        elif rknt == 2:
                            sc2.write(
                                f"ü•à | {leaderboard[vkey]['NameCountry']}: :red[{leaderboard[vkey]['HighestScore']}]")
                        elif rknt == 3:
                            sc3.write(
                                f"ü•â | {leaderboard[vkey]['NameCountry']}: :red[{leaderboard[vkey]['HighestScore']}]")
                        elif rknt == 4:
                            sc3.write(
                                f"4Ô∏è | {leaderboard[vkey]['NameCountry']}: :red[{leaderboard[vkey]['HighestScore']}]")



